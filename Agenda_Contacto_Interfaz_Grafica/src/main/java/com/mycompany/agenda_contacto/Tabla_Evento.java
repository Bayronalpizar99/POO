    public void CargarDatos(){
        Eventos a;
        for(int i= 0; i < Evento.contenedor.size(); i++){
        a=(Eventos)Evento.contenedor.get(i);
        modelo.insertRow(contador, new Object[]{});
        modelo.setValueAt(a.getNombre(),contador,0);
        modelo.setValueAt(a.getLugar(),contador,1);
        modelo.setValueAt(a.getDia(),contador,2);
        modelo.setValueAt(a.getHora(),contador,3);
        modelo.setValueAt(a.getMotivo(),contador,4);
        contador++;
        }
}
